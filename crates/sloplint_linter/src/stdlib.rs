//! The Python standard-library top-level module set, bundled for offline determinism.
//!
//! SLP180 must never flag a stdlib import as "undeclared". Rather than query the host
//! interpreter (non-deterministic, network/host dependent), we ship the set of top-level
//! standard-library module names for the pinned Python (3.12 `sys.stdlib_module_names`,
//! the broadest recent release — a name that is stdlib in an older version stays
//! recognized, biasing toward false negatives, which is the conservative direction).
//!
//! The list is sorted so lookups are a binary search. Common private modules that user
//! code legitimately imports (`__future__`, `_thread`) are included.

