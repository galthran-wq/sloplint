/// Whether `module` is a top-level standard-library module for the pinned Python.
pub fn is_stdlib(module: &str) -> bool {
    STDLIB.binary_search(&module).is_ok()
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn stdlib_list_is_sorted_for_binary_search() {
        let mut sorted = STDLIB.to_vec();
        sorted.sort_unstable();
        assert_eq!(sorted, STDLIB, "STDLIB must stay sorted");
    }

    #[test]
    fn recognizes_common_stdlib_modules() {
        for m in ["os", "sys", "json", "asyncio", "dataclasses", "__future__"] {
            assert!(is_stdlib(m), "{m} should be stdlib");
        }
    }

    #[test]
    fn rejects_third_party_names() {
        for m in ["requests", "numpy", "cv2", "definitely_not_a_module"] {
            assert!(!is_stdlib(m), "{m} should not be stdlib");
        }
    }
}
